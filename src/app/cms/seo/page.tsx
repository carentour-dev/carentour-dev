const clampLlmsPriority = (value: number) =>
  Math.max(-100, Math.min(100, Math.trunc(value)));

const toEffectiveValue = (
  overrideValue: string,
  autogeneratedValue: string,
) => {
  const override = trimToText(overrideValue);
  if (override.length > 0) {
    return override;
  }

  const autogenerated = trimToText(autogeneratedValue);
  return autogenerated.length > 0 ? autogenerated : null;
};

const formatAutogeneratedValue = (
  value: string | number | boolean | null | undefined,
  fallback = "None",
) => {
  if (typeof value === "boolean") {
    return value ? "Yes" : "No";
  }
  if (typeof value === "number") {
    return `${value}`;
  }
  if (typeof value === "string") {
    const trimmed = value.trim();
    return trimmed.length > 0 ? trimmed : fallback;
  }

  return fallback;
};

