const readSeoOverrideDrafts = (): Record<string, SeoOverrideDraft> => {
  if (typeof window === "undefined") {
    return {};
  }

  try {
    const raw = window.localStorage.getItem(SEO_DRAFTS_STORAGE_KEY);
    if (!raw) {
      return {};
    }

    const parsed = JSON.parse(raw);
    if (!parsed || typeof parsed !== "object") {
      return {};
    }

    return parsed as Record<string, SeoOverrideDraft>;
  } catch {
    return {};
  }
};

const writeSeoOverrideDrafts = (drafts: Record<string, SeoOverrideDraft>) => {
  if (typeof window === "undefined") {
    return;
  }

  try {
    window.localStorage.setItem(SEO_DRAFTS_STORAGE_KEY, JSON.stringify(drafts));
  } catch {
    // Ignore localStorage failures and keep editing flow uninterrupted.
  }
};

const getSeoHealthBadge = (score: number) => {
  if (score >= 60) {
    return { label: "Needs attention", variant: "destructive" as const };
  }
  if (score >= 30) {
    return { label: "Good", variant: "secondary" as const };
  }

  return { label: "Excellent", variant: "success" as const };
};

const matchesSeoHealthFilter = (score: number, filter: SeoHealthFilter) => {
  if (filter === "needs-attention") {
    return score >= 60;
  }
  if (filter === "good") {
    return score >= 30 && score < 60;
  }
  if (filter === "excellent") {
    return score < 30;
  }

  return true;
};

const formatSourceTypeLabel = (value: string) => {
  if (value === "all") {
    return "All";
  }

  return value
    .split("-")
    .map((part) => (part ? `${part[0]!.toUpperCase()}${part.slice(1)}` : part))
    .join(" ");
};

const formatDraftSavedRelative = (savedAt: string) => {
  const parsed = new Date(savedAt);
  if (Number.isNaN(parsed.getTime())) {
    return "recently";
  }

  const diffSeconds = Math.round((parsed.getTime() - Date.now()) / 1000);
  const absSeconds = Math.abs(diffSeconds);
  const rtf = new Intl.RelativeTimeFormat("en", { numeric: "auto" });

  if (absSeconds < 60) {
    return rtf.format(diffSeconds, "second");
  }
  const diffMinutes = Math.round(diffSeconds / 60);
  if (Math.abs(diffMinutes) < 60) {
    return rtf.format(diffMinutes, "minute");
  }
  const diffHours = Math.round(diffMinutes / 60);
  if (Math.abs(diffHours) < 24) {
    return rtf.format(diffHours, "hour");
  }
  const diffDays = Math.round(diffHours / 24);
  return rtf.format(diffDays, "day");
};

const formatDraftSavedAbsolute = (savedAt: string) => {
  const parsed = new Date(savedAt);
  if (Number.isNaN(parsed.getTime())) {
    return "Unknown time";
  }

  return parsed.toLocaleString();
};

